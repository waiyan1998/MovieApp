
import UIKit

extension UIView {
    @objc public class var identifier: String {
        return String(describing: self)
    }
    
    @objc public class var nibName: String {
        return String(describing: self)
    }
    
    func loadViewFromNib(nib: String) -> UIView? {
        let bundle = Bundle(for: type(of: self))
        let nib = UINib(nibName: nib, bundle: bundle)
        return nib.instantiate(withOwner: self, options: nil).first as? UIView
    }
}

extension UIView {
    func roundedShadow(opacity: Float, offset:CGSize, color: UIColor, cornerRadius: CGFloat) {
        self.layer.shadowColor = color.cgColor
        self.layer.shadowOffset = offset
        self.layer.shadowOpacity = opacity
        self.layer.cornerRadius = cornerRadius
        self.clipsToBounds = true
        self.layer.masksToBounds = false
    }
    
    func roundCorners(corners: UIRectCorner, radius: CGFloat) {
       let path = UIBezierPath(roundedRect: bounds, byRoundingCorners: corners, cornerRadii: CGSize(width: radius, height: radius))
       let mask = CAShapeLayer()
       mask.path = path.cgPath
       layer.mask = mask
    }
    
    func rotate(toValue: Double, count: Float = 0) {
        let rotation : CABasicAnimation = CABasicAnimation(keyPath: "transform.rotation.z")
        rotation.toValue = NSNumber(value: Double.pi * toValue)
        rotation.duration = 1
        rotation.isCumulative = true
        rotation.repeatCount = count
        self.layer.add(rotation, forKey: "rotationAnimation")
    }
}
